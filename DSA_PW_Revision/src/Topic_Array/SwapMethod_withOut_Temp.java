package Topic_Array;

public class SwapMethod_withOut_Temp {
	 public static void main(String[] args) {
		//Swap Method without using temp variable !
			
			// Before swap ;
			
			int a = 3;
			int b = 9;
			

		     System.out.println("value of  a : " + a);
		     System.out.println("value of  b : " + b);
			
			 System.out.println("_________________________________");
			
			// Applying  swap by maths  ;
			
			 a = a + b;
			 b = a - b;
			 a = a - b;
			     
			     System.out.println("value of  a : " + a);
			     System.out.println("value of  b : " + b);		
			
		
	}
}

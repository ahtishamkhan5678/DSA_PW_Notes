package Topic_Array;

public class SwapMethod_ByTemp {
	public static void main(String[] args) {
		//Swap Method below here by using temp variable !
		
		// Before swap ;
		
		int a = 3;
		int b = 9;
		

	     System.out.println("value of  a : " + a);
	     System.out.println("value of  b : " + b);
		
		 System.out.println("**********************************");
		
		// After swap ;
		
		int temp = a;
		     a = b;
		     b = temp;
		     
		     System.out.println("value of  a : " + a);
		     System.out.println("value of  b : " + b);		
		
		
	}

}
